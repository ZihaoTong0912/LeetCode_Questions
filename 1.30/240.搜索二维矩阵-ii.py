# @lc code=start
class Solution:
    def searchMatrix(self, matrix: List[List[int]], target: int) -> bool:
        for i in range(len(matrix)):
            for j in range(len(matrix[0])):
                if matrix[i][-1] < target:
                    break
                if matrix[i][j] == target:
                    return True
        return False
# @lc code=end

